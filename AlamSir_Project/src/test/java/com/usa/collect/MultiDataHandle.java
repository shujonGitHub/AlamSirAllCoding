package com.usa.collect;

public class MultiDataHandle {

	public static void main(String[] args) {
		
		
		int q[][] = { { 1, 7, 9, 12, 15 }, { 19, 21, 23, 25, 27 }, { 29, 31, 33, 39, 45 } };

		for (int r = 0; r < 3; r++) {
			for (int c = 0; c < 5; c++) {

				System.out.print(" " + q[r][c]);
			}
			System.out.println();
		}
		System.out.println("==========================================");
	}

		

	}


