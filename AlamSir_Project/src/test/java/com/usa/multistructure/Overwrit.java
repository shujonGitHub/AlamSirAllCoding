package com.usa.multistructure;

public class Overwrit {

	public static void main(String[] args) {

		Overwrit objc = new Overwrit();

		objc.property();
		objc.cash();
		objc.outcome();

	}

	public void property() {
		System.out.println("first overwriting");
		System.out.println("I am getting money from her");

	}

	public void cash() {
		System.out.println("Second overwriting");
		System.out.println("I get some cash from lady");

	}

	public void outcome() {
		System.out.println("Third overwriting");
		System.out.println("My outcome is i am wealthy ");

	}

}
